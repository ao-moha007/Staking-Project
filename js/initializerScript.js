document.addEventListener("DOMContentLoaded", () => {
  const form = document.getElementById("initForm");
  const status = document.getElementById("status");

  document.getElementById("initBtn").addEventListener("click", async (e) => {

    e.preventDefault();

    try {
      if (!window.ethereum) {
        alert("Please install MetaMask!");
        return;
      }

      const provider = new ethers.BrowserProvider(window.ethereum);
      await provider.send("eth_requestAccounts", []);
      const signer = await provider.getSigner();
      const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);

      const startDate = await contract.getStartDate();
      if (startDate > 0) {
        status.textContent = "Contract already initialized.";
        return;
      }

      const owner = document.getElementById("owner").value;
      const token = document.getElementById("token").value;
      const apy = parseInt(document.getElementById("apy").value);
      const minStake = ethers.parseEther(document.getElementById("minStake").value);
      const maxStake = ethers.parseEther(document.getElementById("maxStake").value);
      const start = parseInt(document.getElementById("startDate").value);
      const end = parseInt(document.getElementById("endDate").value);
      const stakeDays = parseInt(document.getElementById("stakeDays").value);
      const earlyFee = parseInt(document.getElementById("earlyFee").value);

      const tx = await contract.initialize(
        owner, token, apy,
        minStake, maxStake,
        start, end,
        stakeDays, earlyFee
      );

      status.textContent = " Transaction sent. Waiting for confirmation...";
      await tx.wait();
      status.textContent = " Contract initialized successfully!";
      alert(" Contract initialized successfully!");
    } catch (err) {
      console.error(err);
      status.textContent = ` Error: ${err.message}`;
      alert(` Error: ${err.message}`);
    }
  });
});
